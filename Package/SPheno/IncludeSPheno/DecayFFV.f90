LorentzFactor = (Mex1*Mex2*(Mex1**4 + (Mex2**2 - Mex3**2)**2 - 2*Mex1**2*(Mex2**2 + Mex3**2)))/(8._dp*Mex3**2) 
AmpSquared = AmpSquared + LorentzFactor*Amplitude(3)*Conjg(AmplitudeC(4)) 

