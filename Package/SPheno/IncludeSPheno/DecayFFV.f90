LorentzFactor = (Mex1**6 + (Mex2**2 - Mex3**2)**3 - Mex1**4*(Mex2**2 + 3*Mex3**2) - Mex1**2*(Mex2**4 + 2*Mex2**2*Mex3**2 - 3*Mex3**4))/(16._dp*Mex3**2) 
