Real(dp), Intent(in) :: Mex1,Mex2,Mex3 
Complex(dp), Intent(out) :: Amplitude(2) 
Real(dp), Intent(in) :: ML1, ML2, ML3 
Complex(dp), Intent(in) :: coup1L, coup1R, coup2L, coup2R, coup3 
Complex(dp) :: int1,int2,int3,int4,int5,int6,int7,int8,int9,int10 

Amplitude = 0._dp

! Necessary Loop Integrals 
int1=SA_Cget("C0  ",Mex2**2,Mex3**2,Mex1**2,ML1**2,ML3**2,ML2**2)
int2=SA_Cget("C1  ",Mex2**2,Mex3**2,Mex1**2,ML1**2,ML3**2,ML2**2)
int3=SA_Cget("C2  ",Mex2**2,Mex3**2,Mex1**2,ML1**2,ML3**2,ML2**2)


! Now the full amplitude for the different Lorentz Parts 
! Coefficient of Mat[<v1|6|v2>] 
Amplitude(1)= & 
& +(2*(0.,1._dp)*coup1L*coup2L*coup3*int3*Mex1) & 
& +(2*(0.,1._dp)*coup1R*coup2R*coup3*int2*Mex2) & 
& +(4*(0.,1._dp)*coup1R*coup2L*coup3*int1*ML1)



! Coefficient of Mat[<v1|7|v2>] 
Amplitude(2)= & 
& +(2*(0.,1._dp)*coup1R*coup2R*coup3*int3*Mex1) & 
& +(2*(0.,1._dp)*coup1L*coup2L*coup3*int2*Mex2) & 
& +(4*(0.,1._dp)*coup1L*coup2R*coup3*int1*ML1)


If ((Abs(Amplitude(1))).ne.(Abs(Amplitude(1)))) Then
Amplitude=0._dp
End if

!Field Order 
! FieldOrder[1,{F, V, V},1]={Internal[1][i1], External[1][gt1], AntiField[Internal[2]][i2]}; 
! FieldOrder[1,{F, V, V},2]={External[2][gt2], AntiField[Internal[1]][i1], Internal[3][i3]}; 
! FieldOrder[1,{F, V, V},3]={External[3][gt3], Internal[2][i2], AntiField[Internal[3]][i3]}; 
! ExtraSign[1,{F, V, V},1]=1; 
! ExtraSign[1,{F, V, V},2]=1; 
! ExtraSign[1,{F, V, V},3]=1; 
End Subroutine Amp_VERTEX_FtoFS_Topology1_FVV

Subroutine SquareAmp_FtoFS(Mex1,Mex2,Mex3,Amplitude,AmplitudeC,AmpSquared)
Implicit None 
Real(dp), Intent(in) :: Mex1,Mex2,Mex3 
Complex(dp), Intent(in) :: Amplitude(2), AmplitudeC(2) 
Real(dp), Intent(out) :: AmpSquared 
Real(dp) :: LorentzFactor 
 
AmpSquared = 0._dp 

! Mat[<v1|6|v2>] x Mat[<v1|6|v2>]
LorentzFactor = (Mex1**2 + Mex2**2 - Mex3**2)/4._dp 
AmpSquared = AmpSquared + LorentzFactor*Amplitude(1)*Conjg(AmplitudeC(1)) 

! Mat[<v1|6|v2>] x Mat[<v1|7|v2>]
LorentzFactor = (Mex1*Mex2)/2._dp 
AmpSquared = AmpSquared + LorentzFactor*Amplitude(1)*Conjg(AmplitudeC(2)) 

! Mat[<v1|7|v2>] x Mat[<v1|6|v2>]
LorentzFactor = (Mex1*Mex2)/2._dp 
AmpSquared = AmpSquared + LorentzFactor*Amplitude(2)*Conjg(AmplitudeC(1)) 

! Mat[<v1|7|v2>] x Mat[<v1|7|v2>]
LorentzFactor = (Mex1**2 + Mex2**2 - Mex3**2)/4._dp 
AmpSquared = AmpSquared + LorentzFactor*Amplitude(2)*Conjg(AmplitudeC(2)) 
End Subroutine SquareAmp_FtoFS
